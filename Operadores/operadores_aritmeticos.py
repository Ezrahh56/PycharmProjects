# Operadores Aritméticos

a = 10
b = 3

# Suma +
suma = a + b
print(f'Suma:{suma}')

# Resta
resta = a - b
print(f'Resta:{resta}')

#Multiplicación
multiplicacion = a * b
print(f'Multiplicación:{multiplicacion}')

#division / recorta un tipo float
division = a / b
print(f'División:{division}')

# División entera
division_entera = a // b
print(f'División entera:{division_entera}')

#Módulo (%)
modulo = a % b
print(f'Módulo:{modulo}')

# Exponente (potencia) **
exponente = a ** b
print(f'Exponente:{exponente}')

